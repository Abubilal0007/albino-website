    // FAQ search function
function filterFAQ() {
    let input = document.getElementById('faqSearch').value.toLowerCase();
    let faqItems = document.getElementsByClassName('faq-item');
  
    for (let i = 0; i < faqItems.length; i++) {
      let question = faqItems[i].getElementsByClassName('faq-question')[0];
      if (question.innerHTML.toLowerCase().includes(input)) {
        faqItems[i].style.display = "";
      } else {
        faqItems[i].style.display = "none";
      }
    }
  }
  
  // Toggle FAQ answer display
  document.querySelectorAll('.faq-question').forEach(item => {
    item.addEventListener('click', () => {
      item.classList.toggle('active');
    });
  });



