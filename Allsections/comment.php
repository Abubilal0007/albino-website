function changeLanguage(lang) {
    // Change the text content of elements with data attributes
    const elements = document.querySelectorAll('[data-en]');
    elements.forEach(el => {
        el.textContent = el.getAttribute(`data-${lang}`);
    });

    // Change the text of the Read More buttons
    const readMoreButtons = document.querySelectorAll('.read-more-btn');
    readMoreButtons.forEach(btn => {
        btn.textContent = btn.getAttribute(`data-lang-${lang}`);
    });
}

// Example of usage for a language selector
document.querySelector('.language-select').addEventListener('change', (event) => {
    changeLanguage(event.target.value);
});

// Event listeners for the individual language buttons
document.getElementById('arabic-button').addEventListener('click', function() {
    changeLanguage('ar');
});

